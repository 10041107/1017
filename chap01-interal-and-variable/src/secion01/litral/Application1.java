package secion01.litral;

public class Application1 {
    public static void main(String[] args) {
        System.out.printf("Hello and welcome!");

        /*
        범위주석
        * */
        //한줄주석

        // 여러 가지 값의 형태를 출력할 수 있다.

        //목차 1. 숫자 형태의 값
        System.out.println(123); // 정수형 리터럴

        //print, printf: 화면에 출력. 다음 줄에 이어서 출력해줌
        //println: 화면에 출력. 다음 문단에 이어서 출력해줌
        System.out.println(1.23); //실수형 리터럴 ( 리터럴 = 값 그 차제)

        //목차 2. 문자 형태의 값 출력
        //const $input = document.getElementById('id'); << 엘리먼트값. 이 값 자체를 가져온다는 소리
        System.out.println('a'); // 문자열 리터럴
        //System.out.println('ab'); //홑따옴표: 문자(a)만 출력 가능. 문자열(ab)은 출력 불가. 두 개 이상은 문자로 취급X
        System.out.println('1'); // 문자

        //목차 3. 문자열 형태의 값 출력

        System.out.println("안녕하세요"); // 쌍따옴표: 문자열형

        System.out.println('안'+'녕'+'하'+'세'+'요'); // = 문자배열 (=문자열)

        //목차 4. 논리 형태의 값 출력

        System.out.println(true); // = 불리언
        System.out.println(false);











        }
    }